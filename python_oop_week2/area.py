print("โปรแกรมหาพื้นที่ 4 เหลี่ยมจัตุรัส")
a = int(input('ใส่ค่าด้าน : '))
aa = a*a
print(f'ค่าพื้นที่ของ 4 เหลี่ยมจัตุรัส = {aa}')

print("โปรแกรมหาพื้นที่ 4 เหลี่ยมผืนผ้า")
Width = int(input('ใส่ค่ากว้าง'))
Length = int(input('ใส่ค่ายาว'))
bbb = Width * Length
print(f'ค่าพื้นที่ของ 4 เหลี่ยมผืนผ้า = {bbb}')

print("โปรแกรมหาพื้นที่ วงกลม ")
